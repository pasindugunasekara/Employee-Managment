    private void loadAllCurrentlyJob() {
        
        try {
            
            List<Job> jobList= new TotalCurrentlyJob().getAllJobs();
            
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            
            for(Job tempJob:jobList){
                model.addRow(new Object[]{
                    tempJob.getEp_id(),
                    tempJob.getDesignation(),
                    tempJob.getDistrict_Office(),
                    tempJob.getDivisional_Office(),
              
               });
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TotalCurrentlyEmployee.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TotalCurrentlyEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
     
        
    }
