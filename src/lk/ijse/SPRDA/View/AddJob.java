    private void btnAddJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddJobActionPerformed
        
        try {
            String Ep_id=txtEmp_ID.getText();
            String Designation=cmbDesignation.getSelectedItem().toString();
            String District_Office=cmbDistrictOffice.getSelectedItem().toString();
            String Divisional_Office=txtDivisionalOffice.getText();
            
            Job j1=new Job(
                    Ep_id,
                    Designation,
                    District_Office,
                    Divisional_Office
            );
            
            
            
            boolean isSaved=new JobController().saveJob(j1);
            
            if(isSaved){
                JOptionPane.showMessageDialog(null, "Saved");
            }else{
                JOptionPane.showMessageDialog(null, "Try again");
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddJob.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddJob.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAddJobActionPerformed

